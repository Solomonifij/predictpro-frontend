import { Sparkles, TrendingUp, Minus } from "lucide-react"
import { cn } from "@/lib/utils"
import type { Match } from "@/lib/types"

export function AIAnalysisPanel({ match }: { match: Match }) {
  const { prediction } = match

  function favorLabel(favors: "home" | "away" | "neutral") {
    if (favors === "home") return match.home.shortName
    if (favors === "away") return match.away.shortName
    return "Even"
  }

  return (
    <section className="overflow-hidden rounded-2xl bg-foreground text-background">
      <div className="flex items-center gap-2 border-b border-white/10 px-5 py-4">
        <span className="flex h-8 w-8 items-center justify-center rounded-lg bg-white/10">
          <Sparkles className="h-4 w-4 text-success" aria-hidden="true" />
        </span>
        <div>
          <h2 className="text-sm font-bold">AI Match Analysis</h2>
          <p className="text-xs text-background/60">Generated by PredictPro model v3.2</p>
        </div>
      </div>

      <div className="space-y-5 p-5">
        <p className="text-pretty text-sm leading-relaxed text-background/90">{prediction.analysis}</p>

        <div>
          <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-background/60">Key factors</h3>
          <ul className="space-y-2">
            {prediction.factors.map((f) => (
              <li
                key={f.label}
                className="flex items-center justify-between gap-3 rounded-lg bg-white/5 px-3 py-2 text-sm"
              >
                <span className="flex items-center gap-2 text-background/80">
                  {f.favors === "neutral" ? (
                    <Minus className="h-3.5 w-3.5 text-background/50" />
                  ) : (
                    <TrendingUp className="h-3.5 w-3.5 text-success" />
                  )}
                  {f.label}
                </span>
                <span className="flex items-center gap-2">
                  <span className="text-background/70">{f.value}</span>
                  <span
                    className={cn(
                      "rounded px-1.5 py-0.5 text-[11px] font-bold",
                      f.favors === "neutral" ? "bg-white/10 text-background/70" : "bg-success/20 text-success",
                    )}
                  >
                    {favorLabel(f.favors)}
                  </span>
                </span>
              </li>
            ))}
          </ul>
        </div>
      </div>
    </section>
  )
}
